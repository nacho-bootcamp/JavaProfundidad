package Seccion3;

public class OperadoresDeComparacion {
  public static void main(String[] args) {
    double sueldo1 = 100000.51213;
    double sueldo2 = 4132151.151551;
    System.out.println(sueldo1 > sueldo2);
    System.out.println(sueldo1 < sueldo2);
    System.out.println(sueldo1 >= sueldo2);
    System.out.println(sueldo1 <= sueldo2);
    System.out.println(sueldo1 != sueldo2);
    System.out.println(sueldo1 == sueldo2);
  }
}
