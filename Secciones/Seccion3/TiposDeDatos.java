package Seccion3;

public class TiposDeDatos {
  public static void main(String args[]) {
    // tipo de datos / identificador = literal
    // con final definimos una constante y el literal no debe cambiar
    final int edad = 18;
    System.out.println("Mi edad es :" + edad);
    @SuppressWarnings("unused")
    byte tamanio = 50;
    @SuppressWarnings("unused")
    char caracter = 'C';
    @SuppressWarnings("unused")
    short superficieHabitable = 1023;
    @SuppressWarnings("unused")
    long distancia = 50505051L;
    @SuppressWarnings("unused")
    boolean isActive = false;
    @SuppressWarnings("unused")
    float sueldo = 100000.015F;
    @SuppressWarnings("unused")
    double superficie = 1000000000000.25555;
  }
}
