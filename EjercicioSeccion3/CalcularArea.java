package EjercicioSeccion3;

public class CalcularArea {

  public static void main(String[] args) {
    // calcular el area de un cuadrado

    int AreaCuadrado = 4 * 4;
    System.out.println(AreaCuadrado);

    // calcular el area de un circulo

    double AreaCirculo = Math.PI * (23.5 * 2);
    System.out.println(AreaCirculo);

    // calcular el area de un rectangulo

    int AreaRectangulo = 20 * 19;
    System.out.println(AreaRectangulo);

    // calcular el area de un triangulo

    double AreaTriangulo = (15.0 * 30.0) / 2.0;
    System.out.println(AreaTriangulo);
  }
}
